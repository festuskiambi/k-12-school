class Teacher(object):
    def __init__(self):

        #class variables
        self.fName=None
        self.sName=None
        self.gradeLevel=None
    #get fName
    def getfName(self):
        return self.fName
    #set sName
    def setfName(self,fName):
        self.fName = fName
    #get sName
    def getsName(self):
        return self.sName
    #set sName
    def setsName(self,sName):
        self.sName = sName
    #get gradeLevel
    def getgradeLevel(self):
        return self.gradeLevel
    #set gradeLevel
    def setgradeLevel(self,gradeLevel):
        self.gradeLevel = gradeLevel
